export function detectLanguage(fallback: SupportedLang = 'es'): SupportedLang {
  if (typeof navigator === 'undefined') return fallback
  const browserLang = navigator.language?.toLowerCase() || ''
  const langCode = browserLang.split('-')[0]
  if (langCode in translations) return langCode as SupportedLang
  return fallback
}

export function t(lang: SupportedLang, key: string, vars?: Record<string, string>): string {
  let text = translations[lang]?.[key] || translations['es'][key] || key
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v)
    })
  }
  return text
}
